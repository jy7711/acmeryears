#include <bits/stdc++.h>
using namespace std;
using ll = long long;
void ac() 
{
    int n; cin >> n;
    for (int i = 1; i <= n; i++) cout << i << ' ';
    cout << '\n';
    return;
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
    int t = 1; cin >> t;
    while (t--) {
        ac();
    }
    return 0;
}