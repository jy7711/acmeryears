#include <stdio.h>
int main()
{
    int lr;
    scanf("%d", &lr);
    int ans = 0;
    if (lr <= 100000)
        ans = lr * 0.1;
    else if (lr <= 200000)
        ans = (lr - 100000) * 0.075 + 100000 * 0.1;
    else if (lr <= 400000)
        ans = (lr - 200000) * 0.05 + 100000 * 0.075 + 100000 * 0.1;
    else if (lr <= 600000)
        ans = (lr - 400000) * 0.03 + 200000 * 0.05 + 100000 * 0.075 +
              100000 * 0.1;
    else if (lr <= 1000000)
        ans = (lr - 600000) * 0.015 + 200000 * 0.03 + 200000 * 0.05 +
              100000 * 0.075 + 100000 * 0.1;
    else
        ans = (lr - 1000000) * 0.01 + 400000 * 0.015 + 200000 * 0.03 +
              200000 * 0.05 + 100000 * 0.075 + 100000 * 0.1;
    printf("%d", ans);
}



